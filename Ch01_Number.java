public class Ch01_Number {
  // 데이터 타입: 숫자
  public static void main(String[] args) {
    System.out.println("1 + 2 = " + (1 + 2)); // 더하기
    System.out.println("1 - 2 = " + (1 - 2)); // 빼기
    System.out.println("1.2 + 1.3 = " + (1.2 + 1.3));
    System.out.println("2 * 3 = " + (2 * 3)); // 곱하기
    System.out.println("6 / 2 = " + (6 / 2)); // 나누기
    System.out.println("6 % 2 = " + (6 % 2)); // 나머지
  }
}